const detailTranslations = {
  fr: {

  },
  ar: {

  }
};

let currentLanguage = localStorage.getItem('language') || 'fr';

document.addEventListener('DOMContentLoaded', () => {
  setLanguage(currentLanguage);
  
  const languageToggle = document.getElementById('language-toggle');
  if (languageToggle) {
    languageToggle.addEventListener('click', () => {
      currentLanguage = currentLanguage === 'fr' ? 'ar' : 'fr';
      setLanguage(currentLanguage);
      localStorage.setItem('language', currentLanguage);
      window.location.reload();
    });
  }
});

function setLanguage(lang) {
  const html = document.documentElement;
  
  html.setAttribute('lang', lang);
  html.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  
  const nextLangSpan = document.getElementById('next-lang');
  if (nextLangSpan) {
    nextLangSpan.textContent = lang === 'fr' ? 'AR' : 'FR';
  }
  
  document.title = translations[lang]['page-title'] || document.title;
  
  const translatableElements = document.querySelectorAll('[data-translate]');
  translatableElements.forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });
  
  document.body.classList.toggle('rtl', lang === 'ar');
  document.body.classList.toggle('ltr', lang === 'fr');
}

function getTranslation(key) {
  return translations[currentLanguage][key] || key;
}

function getDetailTranslation(key) {
  return detailTranslations[currentLanguage][key] || key;
}

function translateValue(value) {
  return detailTranslations[currentLanguage][value] || value;
}

window.getTranslation = getTranslation;
window.getDetailTranslation = getDetailTranslation;
window.getCurrentLanguage = () => currentLanguage;
window.translateValue = translateValue;